# Faça um programa que leia um número inteiro e mostre na tela seu sucessor e seu antecessor.

print('Olá, sou o BOB, me diga um número que eu te mostro o antecessor e o sucessor dele!')
n = int(input('Agora, digite um número : '))
at = n-1
su = n+1
print('O número digitado foi : {}, seu antecessor é: {}, e seu sucessor é: {}'.format(n, at, su))
print('Obrigado, tenha um ótimo dia!')
